#' Add together two numbers
#'
#' `add()` adds together two numbers. Features of this function:
#' * Smells nice
#' * Has good vibes
#'
#' @param x A number. I like `add()`, because it's great. It's obvious that
#'   `add()` belongs to the same package as [set_mfl()]. For some irrelevant
#'   information, see `vignette("rd-formatting", package = "roxygen2")`
#' @param y A number.
#' @returns A numeric vector.
#' @examples
#' add(1, 1)
#' add(10, 1)
#'
#' @export
add <- function(x, y) {
  x + y
}
